// Тест-кейсы
// 1. Успешное выполнение:
//     Вход: `sayHello()`
//       Ожидаемый результат: `'Привет, Иван Иванов, тебе 10 лет.'`
//     Вход: `sayHello('Дима')`
//       Ожидаемый результат: `'Привет, Дима Иванов, тебе 10 лет.'`
//     Вход: `sayHello('Евгений', 'Петров', 25, 'Здравствуйте, ')`
//       Ожидаемый результат: `'Здравствуйте, Евгений Петров, тебе 25 лет.'`

// 2. Неуспешное выполнение:
//    - Вход: `sayHello(null, null, null, null)`
//      - Ожидаемый результат: `'Привет, Иван Иванов, тебе 10 лет.'` (параметры игнорируются, используются значения по умолчанию)
//    - Вход: `sayHello(undefined, undefined, undefined, undefined)`
//      - Ожидаемый результат: `'Привет, Иван Иванов, тебе 10 лет.'` (параметры игнорируются, используются значения по умолчанию)

// Граничные случаи (corner cases)

// 1. Вход: `sayHello('', '', 0, '')`
//     Ожидаемый результат: `' Привет,  , тебе 0 лет.'` (проверка обработки пустых строк и нуля)
   
// 2. Вход: `sayHello('Алексей', 'Смирнов', -1, 'Привет, ')`
//    Ожидаемый результат: `'Привет, Алексей Смирнов, тебе -1 лет.'` (проверка обработки отрицательного возраста)

// 3. Вход: `sayHello(' ', ' ', 100, 'Привет, ')`
//    Ожидаемый результат: `'Привет,   , тебе 100 лет.'` (проверка обработки пробелов)



const sayHello = (
  name = 'Иван',
  surname = 'Иванов',
  age = 10,
  greeting = 'Привет, ',
) => (
  greeting + name + ' ' + surname + ', тебе ' + age + ' лет.'
);

// Функция для тестирования
const runTests = () => {
  const tests = [
    {
      input: [],
      expected: 'Привет, Иван Иванов, тебе 10 лет.'
    },
    {
      input: ['Дима'],
      expected: 'Привет, Дима Иванов, тебе 10 лет.'
    },
    {
      input: ['Евгений', 'Петров', 25, 'Здравствуйте, '],
      expected: 'Здравствуйте, Евгений Петров, тебе 25 лет.'
    },
    {
      input: [null, null, null, null],
      expected: 'Привет, Иван Иванов, тебе 10 лет.'
    },
    {
      input: [undefined, undefined, undefined, undefined],
      expected: 'Привет, Иван Иванов, тебе 10 лет.'
    },
    {
      input: ['', '', 0, ''],
      expected: ' Привет,  , тебе 0 лет.'
    },
    {
      input: ['Алексей', 'Смирнов', -1, 'Привет, '],
      expected: 'Привет, Алексей Смирнов, тебе -1 лет.'
    },
    {
      input: [' ', ' ', 100, 'Привет, '],
      expected: 'Привет,   , тебе 100 лет.'
    },
  ];

  tests.forEach((test, index) => {
    const result = sayHello(...test.input);
    const isTestPassed = result === test.expected;
    console.log(`Test ${index + 1}: ${isTestPassed ? 'Passed' : 'Failed'}`);
    if (!isTestPassed) {
      console.log(`  Expected: "${test.expected}", but got: "${result}"`);
    }
  });
};

// Запуск тестов
runTests();
